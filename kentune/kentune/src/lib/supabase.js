import { createClient } from '@supabase/supabase-js';

const supabaseUrl = import.meta.env?.VITE_SUPABASE_URL;
const supabaseAnonKey = import.meta.env?.VITE_SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseAnonKey) {
  throw new Error('Missing Supabase environment variables. Please check your .env file for VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY');
}

export const supabase = createClient(supabaseUrl, supabaseAnonKey, {
  auth: {
    autoRefreshToken: true,
    persistSession: true,
    // Ensure the redirect URL uses the current origin instead of localhost
    redirectTo: `${typeof window !== 'undefined' ? window.location?.origin : ''}/verify-email`,
    // Additional options for better email verification handling
    detectSessionInUrl: true,
    flowType: 'pkce'
  }
});